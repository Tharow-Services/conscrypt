 * WITHOUT WARRANTIES OR CONDITIONS OF ANY KIND, either express or implied.
 * See the License for the specific language governing permissions and
 * limitations under the License.
 */

 /* This file was generated by generate_blocklist.sh. */

HEADER

cat <<CLASS_DEFINITION
package ${package};

final class StaticBlocklist {
  static final String[] PUBLIC_KEYS = {
CLASS_DEFINITION

while read hash; do
  echo "    \"${hash}\","
done < ${blocklist}

cat <<CLASS_EPILOGUE
  };
}
CLASS_EPILOGUE
