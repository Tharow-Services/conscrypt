# This program generates StaticBlocklist.java based on the static list of
# blocked public keys.

blocklist="$1"
if [ -z ${blocklist} ]; then
  echo "usage: generate_blocklist.sh <blocklist.txt> [package_name]"
  exit 1
fi

package="$2"
if [ -z ${package} ]; then
  package="org.conscrypt"
fi

cat <<HEADER
/*
